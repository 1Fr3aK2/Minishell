#include "libft.h"

long	ft_atol(const char *str)
{
	long	number;
	int		sign;

	number = 0;
	sign = 1;
	if (!str)
		return (-1);
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		number *= 10;
		number += *str - 48;
		str++;
	}
	return (number * sign);
}
